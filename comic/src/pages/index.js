import Head from 'next/head';
import Image from 'next/legacy/image';
import styles from '@/styles/Comic.module.css';
import { Button } from '../components/button';
import { Detail } from '../components/detail';
import comics from '../data/comics.json';


export default function Home() {
	
	function get_creators (creators) {
		const names = creators.map( creator => creator.name.split(' ')[1])
		return names.join(', ')
	}
	return (
		<>
			<Head>
				<title>Comic Cards</title>
				<meta name='description' content='Generated by create next app' />
				<meta name='viewport' content='width=device-width, initial-scale=1' />
				<link rel='icon' href='/favicon.ico' />
			</Head>

			<main className={styles.slides}>
				{comics?.map((card, key) => {
					return (
						<article key={key} className="card">
							<div className={styles.imgCont}>
								<Image 
									className={styles.image}
									alt={card.title} 
									src={card.thumbnail}
									blurDataURL={card.thumbnail}
									placeholder="blur"
									width={310} 
									height={475}
									layout="responsive"/>
								<Button
									link={card.thumbnail}/>
							</div>
							<div className='content-section'>
								<h2>{card.title}</h2>
								<Detail 
									issue={+card.issueNumber} 
									date={card.publishDate && new Date(card.publishDate)} 
									creators={get_creators(card.creators)}/>
							</div>
						</article>
					);
				})}
			</main>
		</>
	);
}
